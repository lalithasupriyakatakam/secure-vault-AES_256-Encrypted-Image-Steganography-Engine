const uint8_t AES256::rcon[11] = {
    0x00,0x01,0x02,0x04,0x08,0x10,0x20,0x40,0x80,0x1b,0x36
};

uint8_t AES256::gmul(uint8_t a, uint8_t b) {
    uint8_t p = 0;
    for (int i = 0; i < 8; i++) {
        if (b & 1) p ^= a;
        bool hiBit = (a & 0x80);
        a <<= 1;
        if (hiBit) a ^= 0x1b;
        b >>= 1;
    }
    return p;
}

void AES256::keyExpansion(const uint8_t* key, uint8_t roundKeys[240]) {
    memcpy(roundKeys, key, 32);
    for (int i = Nk; i < Nb*(Nr+1); i++) {
        uint8_t temp[4];
        memcpy(temp, roundKeys+(i-1)*4, 4);
        if (i % Nk == 0) {
            uint8_t t = temp[0];
            temp[0] = sbox[temp[1]] ^ rcon[i/Nk];
            temp[1] = sbox[temp[2]];
            temp[2] = sbox[temp[3]];
            temp[3] = sbox[t];
        } else if (i % Nk == 4) {
            for (int j=0;j<4;j++) temp[j]=sbox[temp[j]];
        }
        for (int j=0;j<4;j++)
            roundKeys[i*4+j] = roundKeys[(i-Nk)*4+j] ^ temp[j];
    }
}

void AES256::addRoundKey(uint8_t state[4][4], const uint8_t* rk) {
    for (int c=0;c<4;c++)
        for (int r=0;r<4;r++)
            state[r][c] ^= rk[c*4+r];
}

void AES256::subBytes(uint8_t state[4][4]) {
    for (int r=0;r<4;r++) for(int c=0;c<4;c++) state[r][c]=sbox[state[r][c]];
}

void AES256::invSubBytes(uint8_t state[4][4]) {
    for (int r=0;r<4;r++) for(int c=0;c<4;c++) state[r][c]=inv_sbox[state[r][c]];
}

void AES256::shiftRows(uint8_t state[4][4]) {
    uint8_t tmp;
    tmp=state[1][0]; state[1][0]=state[1][1]; state[1][1]=state[1][2];
    state[1][2]=state[1][3]; state[1][3]=tmp;
    tmp=state[2][0]; state[2][0]=state[2][2]; state[2][2]=tmp;
    tmp=state[2][1]; state[2][1]=state[2][3]; state[2][3]=tmp;
    tmp=state[3][3]; state[3][3]=state[3][2]; state[3][2]=state[3][1];
    state[3][1]=state[3][0]; state[3][0]=tmp;
}

void AES256::invShiftRows(uint8_t state[4][4]) {
    uint8_t tmp;
    tmp=state[1][3]; state[1][3]=state[1][2]; state[1][2]=state[1][1];
    state[1][1]=state[1][0]; state[1][0]=tmp;
    tmp=state[2][0]; state[2][0]=state[2][2]; state[2][2]=tmp;
    tmp=state[2][1]; state[2][1]=state[2][3]; state[2][3]=tmp;
    tmp=state[3][0]; state[3][0]=state[3][1]; state[3][1]=state[3][2];
    state[3][2]=state[3][3]; state[3][3]=tmp;
}

void AES256::mixColumns(uint8_t state[4][4]) {
    for (int c=0;c<4;c++) {
        uint8_t s0=state[0][c],s1=state[1][c],s2=state[2][c],s3=state[3][c];
        state[0][c]=gmul(0x02,s0)^gmul(0x03,s1)^s2^s3;
        state[1][c]=s0^gmul(0x02,s1)^gmul(0x03,s2)^s3;
        state[2][c]=s0^s1^gmul(0x02,s2)^gmul(0x03,s3);
        state[3][c]=gmul(0x03,s0)^s1^s2^gmul(0x02,s3);
    }
}

void AES256::invMixColumns(uint8_t state[4][4]) {
    for (int c=0;c<4;c++) {
        uint8_t s0=state[0][c],s1=state[1][c],s2=state[2][c],s3=state[3][c];
        state[0][c]=gmul(0x0e,s0)^gmul(0x0b,s1)^gmul(0x0d,s2)^gmul(0x09,s3);
        state[1][c]=gmul(0x09,s0)^gmul(0x0e,s1)^gmul(0x0b,s2)^gmul(0x0d,s3);
        state[2][c]=gmul(0x0d,s0)^gmul(0x09,s1)^gmul(0x0e,s2)^gmul(0x0b,s3);
        state[3][c]=gmul(0x0b,s0)^gmul(0x0d,s1)^gmul(0x09,s2)^gmul(0x0e,s3);
    }
}

void AES256::encryptBlock(const uint8_t in[16], uint8_t out[16],
                           const uint8_t roundKeys[240]) {
    uint8_t state[4][4];
    for (int r=0;r<4;r++) for(int c=0;c<4;c++) state[r][c]=in[c*4+r];

    addRoundKey(state, roundKeys);
    for (int round=1; round<Nr; round++) {
        subBytes(state);
        shiftRows(state);
        mixColumns(state);
        addRoundKey(state, roundKeys+round*16);
    }
    subBytes(state);
    shiftRows(state);
    addRoundKey(state, roundKeys+Nr*16);

    for (int r=0;r<4;r++) for(int c=0;c<4;c++) out[c*4+r]=state[r][c];
}

void AES256::decryptBlock(const uint8_t in[16], uint8_t out[16],
                           const uint8_t roundKeys[240]) {
    uint8_t state[4][4];
    for (int r=0;r<4;r++) for(int c=0;c<4;c++) state[r][c]=in[c*4+r];

    addRoundKey(state, roundKeys+Nr*16);
    for (int round=Nr-1; round>=1; round--) {
        invShiftRows(state);
        invSubBytes(state);
        addRoundKey(state, roundKeys+round*16);
        invMixColumns(state);
    }
    invShiftRows(state);
    invSubBytes(state);
    addRoundKey(state, roundKeys);

    for (int r=0;r<4;r++) for(int c=0;c<4;c++) out[c*4+r]=state[r][c];
}

std::vector<uint8_t> AES256::pkcs7Pad(const std::vector<uint8_t>& data) {
    size_t pad = 16 - (data.size() % 16);
    std::vector<uint8_t> padded = data;
    for (size_t i=0;i<pad;i++) padded.push_back((uint8_t)pad);
    return padded;
}

std::vector<uint8_t> AES256::pkcs7Unpad(const std::vector<uint8_t>& data) {
    if (data.empty()) return data;
    uint8_t pad = data.back();
    if (pad > 16 || pad == 0) throw std::runtime_error("Invalid PKCS7 padding");
    return std::vector<uint8_t>(data.begin(), data.end()-pad);
}

std::vector<uint8_t> AES256::encrypt(const std::vector<uint8_t>& plaintext,
                                      const std::vector<uint8_t>& key) {
    if (key.size() != 32) throw std::runtime_error("AES-256 requires 32-byte key");
    uint8_t roundKeys[240];
    keyExpansion(key.data(), roundKeys);

    auto padded = pkcs7Pad(plaintext);
    std::vector<uint8_t> ciphertext(padded.size());

    uint8_t iv[16] = {};
    for (size_t i=0; i<padded.size(); i+=16) {
        uint8_t block[16];
        for (int j=0;j<16;j++) block[j] = padded[i+j] ^ iv[j];
        encryptBlock(block, ciphertext.data()+i, roundKeys);
        memcpy(iv, ciphertext.data()+i, 16);
    }
    return ciphertext;
}

std::vector<uint8_t> AES256::decrypt(const std::vector<uint8_t>& ciphertext,
                                      const std::vector<uint8_t>& key) {
    if (key.size() != 32) throw std::runtime_error("AES-256 requires 32-byte key");
    if (ciphertext.size() % 16 != 0) throw std::runtime_error("Invalid ciphertext size");

    uint8_t roundKeys[240];
    keyExpansion(key.data(), roundKeys);

    std::vector<uint8_t> plaintext(ciphertext.size());
    uint8_t iv[16] = {};
    for (size_t i=0; i<ciphertext.size(); i+=16) {
        uint8_t block[16];
        decryptBlock(ciphertext.data()+i, block, roundKeys);
        for (int j=0;j<16;j++) plaintext[i+j] = block[j] ^ iv[j];
        memcpy(iv, ciphertext.data()+i, 16);
    }
    return pkcs7Unpad(plaintext);
}

std::vector<uint8_t> AES256::encryptString(const std::string& message,
                                            const std::vector<uint8_t>& key) {
    return encrypt(std::vector<uint8_t>(message.begin(), message.end()), key);
}

std::string AES256::decryptToString(const std::vector<uint8_t>& ciphertext,
                                     const std::vector<uint8_t>& key) {
    auto plain = decrypt(ciphertext, key);
    return std::string(plain.begin(), plain.end());
}
